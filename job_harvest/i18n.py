def normalize_locale(value: str | None) -> str:
    if not value:
        return DEFAULT_LOCALE
    normalized = value.strip().lower().replace("_", "-")
    if normalized.startswith("ko"):
        return "ko"
    if normalized.startswith("en"):
        return "en"
    return DEFAULT_LOCALE


def resolve_locale(request: Request) -> str:
    query_locale = request.query_params.get("lang")
    if query_locale:
        return normalize_locale(query_locale)

    cookie_locale = request.cookies.get(LANG_COOKIE_NAME)
    if cookie_locale:
        return normalize_locale(cookie_locale)

    accept_language = request.headers.get("accept-language", "")
    for item in accept_language.split(","):
        locale = normalize_locale(item.split(";", 1)[0])
        if locale in SUPPORTED_LOCALES:
            return locale
    return DEFAULT_LOCALE


def translate(locale: str, key: str, **kwargs) -> str:
    language = locale if locale in TRANSLATIONS else DEFAULT_LOCALE
    if key.startswith("site."):
        site_key = key.split(".", 1)[1]
        template = (
            SITE_LABELS.get(language, {}).get(site_key)
            or SITE_LABELS["en"].get(site_key)
            or site_key
        )
    else:
        template = TRANSLATIONS.get(language, {}).get(key) or TRANSLATIONS["en"].get(key) or key
    if not kwargs:
        return template
    return template.format(**kwargs)


